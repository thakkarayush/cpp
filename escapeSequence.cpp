#include <iostream>
using namespace std;

int main() {
    //first way
    cout << "Hello World!" << "\n";
    //second way
    cout << "Hello World!" << endl;
    cout << "\tHello World!" << endl;
    cout << "\\Hello World!\\" << endl;
    cout << "\'Hello World!\'" << endl;
    cout << "\"Hello World!\"" << endl;
    cout << "I am learning C++";
    return 0;
}
